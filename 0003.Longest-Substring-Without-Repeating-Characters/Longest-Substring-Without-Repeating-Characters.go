package leetcode

import "fmt"

// Solution 1 bitmap
func lengthOfLongestSubstring(s string) int {

	if len(s) == 0 {
		return 0
	}
	var bitSet [256]bool
	result, left, right := 0, 0, 0
	for left < len(s) {
		// The bitSet corresponding to the character on the right is marked as true, indicating that this character is repeated at the X position and needs to move forward on the left until X is marked as false
		fmt.Println(bitSet[s[right]])
		if bitSet[s[right]] {
			fmt.Println(s[left])
			bitSet[s[left]] = false
			left++
		} else {
			bitSet[s[right]] = true
			right++
		}
		if result < right-left {
			result = right - left
		}
		if left+result >= len(s) || right >= len(s) {
			break
		}
	}
	return result
}

// Solution two sliding window
func lengthOfLongestSubstringV2(s string) int {
	if len(s) == 0 {
		return 0
	}
	var freq [256]int
	result, left, right := 0, 0, -1

	for left < len(s) {
		if right+1 < len(s) && freq[s[right+1]-'a'] == 0 {
			freq[s[right+1]-'a']++
			right++
		} else {
			freq[s[left]-'a']--
			left++
		}
		result = max(result, right-left+1)
	}
	return result
}

func max(a int, b int) int {
	if a > b {
		return a
	}
	return b
}
